    public void popularTabelaClientes(List<Cliente> pListaDeCliente){
        
        DefaultTableModel modeloTabela= new DefaultTableModel();
        
        //Link entre a nossa tabela e o nosso model
        this.jtbClientes.setModel(modeloTabela);
        
        modeloTabela.addColumn("Id");
        modeloTabela.addColumn("Nome");
        modeloTabela.addColumn("Endere√ßo");
        
        for (Cliente cliente : pListaDeCliente) {
            modeloTabela.addRow(
                    new Object[]{
                        cliente.getIdCliente(),
                        cliente.getNome(),
                        cliente.getEndereco()
                                }            
    );            
    }                
    }

